            </p>
            <p>
                Safe refinement requires the highest-quality materials, known as Crystals, which are rare drops
                from high-level monsters. The Boss Monster <a href="/monsters/death-mage">Death Mage</a> guarantees
                a Crystal drop.
            </p>
            <ul>
                <li>Safe weapon refinement requires Vulkanite Crystal.</li>
                <li>Safe armor refinement requires Gravion Crystal.</li>
                <li>Safe artifact refinement requires Lunaris Crystal.</li>
            </ul>
