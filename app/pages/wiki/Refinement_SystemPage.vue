            <h2>Refinement Tips and Strategies</h2>
            <p>
                Refinement enhances the power and survivability of characters by improving weapons, armor, and
                artifacts. Proper prioritization can maximize efficiency and performance.
            </p>
            <ul>
                <li>
                    Weapons – It is recommended to refine weapons as early as possible. Each refinement level
                    increases Attack and Magic Attack, providing immediate improvements in combat effectiveness.
                </li>
                <li>
                    Armor – Refining armor improves Defence and Magic Defence, increasing survivability
                    against enemies.
                </li>
                <li>
                    Artifacts – Artifacts provide unique refinement bonuses at each level. For example,
                    the Pioneer Artifact Set grants an additional +0.25% experience gain per refinement
                    level, up to 2.25% per piece. With four pieces in a set, these bonuses stack, further
                    enhancing their effect.
                </li>
            </ul>
            <p>
                Weapons should be the highest priority for refinement, then artifacts, and finally armor.
                This ensures characters maintain both offensive and defensive advantages throughout progression.
            </p>

