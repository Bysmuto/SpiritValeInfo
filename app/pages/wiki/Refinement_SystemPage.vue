            <h1 class="page-title">Refinement System</h1>
            <p>
                The Refinement System upgrades artifacts, armor, and weapons through the use of special materials.
                Refinement serves as the primary method of pushing gear beyond base stats, providing higher attack,
                defense, and artifact-specific bonuses.
            </p>

            <h2>How to Refine Items</h2>
            <p>
                Refinement is performed at the <a href="/wiki/Non-Player_Character">Blacksmith</a>
                in <a href="/maps/nevaris">Nevaris</a>.
            </p>
            <p>
                Each item type requires a specific refinement material:
            </p>
            <ul>
                <li><strong>Weapons</strong> → Vulkanite</li>
                <li><strong>Armor</strong> → Gravion</li>
                <li><strong>Artifacts</strong> → Lunaris</li>
            </ul>
            <p>
                All refinement attempts also consume <a href="/wiki/Coin">coins</a>, with the cost scaling upward
                at each refinement level.
            </p>

            <h2>Benefits</h2>
            <p>
                Refinement increases an item’s level, granting additional stats or bonuses depending on item type:
            </p>
            <ul>
                <li>All items with X per Refine gain the listed benefit at each refinement level.</li>
                <li>Armor receives additional Defence and Magic Defence.</li>
                <li>Weapons receive additional Attack and Magic Attack.</li>
                <li>Artifacts unlock their innate Refine Bonus each level.</li>
                <li>Certain Cards also scale with refinement.</li>
            </ul>
            <p>
                <strong>Note:</strong> Refinement does not improve randomly rolled stats, primary attributes, or most
                Card bonuses. Utility gear also receives no benefit from refinement.
            </p>

            <h2>Refinement Progression</h2>
            <p>
                Items receive a numerical update to their name as they are refined, indicating the current
                refinement level.
            </p>
            <ul>
                <li>+1 Novice Boots</li>
                <li>+10 Stormpiercer</li>
            </ul>
            <p>All items start at refinement level +0 and can be improved up to +10.</p>

            <h2>Costs and Failure Risks</h2>
            <p>
                Refinement consumes both materials and <a href="/wiki/coins">coins</a>, with requirements increasing
                at higher levels. Each refinement attempt after the first also carries a risk of failure, which may
                downgrade the item's refinement level by one.
            </p>
            <ul>
                <li>
                    Material costs vary depending on item type and refinement rank. Higher-quality materials remove
                    the risk of downgrade but do not affect success rates.
                </li>
                <li>Gold costs increase progressively with each refinement level.</li>
                <li>
                    All items start at +0. Except at +0, refinement attempts can fail, lowering the refinement
                    level by one if unsuccessful.
                </li>
            </ul>
            <p>
                Safe upgrades consume higher-quality materials to prevent downgrades:
            </p>
            <ul>
                <li><strong>Weapons</strong> → Vulkanite Chunk or Vulkanite Crystal</li>
                <li><strong>Armor</strong> → Gravion Chunk or Gravion Crystal</li>
                <li><strong>Artifacts</strong> → Lunaris Chunk or Lunaris Crystal</li>
            </ul>
            <p>
                However, the upgrade to +10 requires a Crystal as the base material, and with no safety material
                to protect the upgrade.
            </p>

